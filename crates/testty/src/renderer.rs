/// Empty glyph used as a fallback for unsupported characters.
const EMPTY_GLYPH: [u8; 16] = [0; 16];

/// Look up the 8x16 bitmap glyph for a character.
///
/// Returns the glyph for ASCII printable (0x20-0x7E) and box-drawing
/// (U+2500-U+257F) characters. All other characters get an empty glyph.
fn lookup_glyph(character: char) -> &'static [u8; 16] {
    let code = character as u32;

    if (0x20..=0x7E).contains(&code) {
        let index = (code - 0x20) as usize;

        &ASCII_GLYPHS[index]
    } else if (0x2500..=0x257F).contains(&code) {
        let index = (code - 0x2500) as usize;

        &BOX_DRAWING_GLYPHS[index]
    } else {
        &EMPTY_GLYPH
    }
}

