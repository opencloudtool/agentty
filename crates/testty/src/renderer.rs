//! Native frame renderer producing pixel-accurate terminal images.
//!
//! Converts a [`TerminalFrame`] into an [`RgbaImage`] using an embedded
//! 8x16 monospace bitmap font. This removes the VHS dependency for core
//! proof generation and is significantly faster than external tools.
//!
//! The renderer covers ASCII printable characters (0x20-0x7E) and Unicode
//! box-drawing characters (U+2500-U+257F). Characters outside these ranges
//! are rendered as a placeholder glyph.

use image::{Rgba, RgbaImage};

use crate::frame::{CellColor, CellStyle, TerminalFrame};

/// Width of each character cell in pixels.
const CELL_WIDTH: u32 = 8;

/// Height of each character cell in pixels.
const CELL_HEIGHT: u32 = 16;

/// Default foreground color (light gray) used when the terminal reports
/// the default color.
const DEFAULT_FG: CellColor = CellColor {
    red: 204,
    green: 204,
    blue: 204,
};

/// Default background color (near-black) used when the terminal reports
/// the default color.
const DEFAULT_BG: CellColor = CellColor {
    red: 30,
    green: 30,
    blue: 30,
};

/// Render a [`TerminalFrame`] to a pixel image.
///
/// Produces an [`RgbaImage`] of dimensions `(cols * 8, rows * 16)` pixels
/// using the embedded bitmap font. Each terminal cell is rendered with its
/// foreground/background color and style modifiers.
pub fn render_to_image(frame: &TerminalFrame) -> RgbaImage {
    let image_width = u32::from(frame.cols()) * CELL_WIDTH;
    let image_height = u32::from(frame.rows()) * CELL_HEIGHT;
    let mut image = RgbaImage::from_pixel(image_width, image_height, color_to_rgba(DEFAULT_BG));

    for row in 0..frame.rows() {
        for col in 0..frame.cols() {
            let foreground = frame.fg_color(row, col).unwrap_or(DEFAULT_FG);
            let background = frame.bg_color(row, col).unwrap_or(DEFAULT_BG);
            let style = frame.cell_style(row, col).unwrap_or_default();

            let cell_text = cell_text_at(frame, row, col);
            let character = cell_text.chars().next().unwrap_or(' ');

            render_cell(
                &mut image,
                u32::from(col) * CELL_WIDTH,
                u32::from(row) * CELL_HEIGHT,
                character,
                foreground,
                background,
                style,
            );
        }
    }

    image
}

/// Extract the text content of a single cell from the frame.
///
/// Delegates to [`TerminalFrame::cell_text()`] which avoids the region
/// overhead of [`TerminalFrame::text_in_region()`].
fn cell_text_at(frame: &TerminalFrame, row: u16, col: u16) -> &str {
    frame.cell_text(row, col)
}

/// Render a single terminal cell into the image at the given pixel offset.
///
/// Handles style modifiers: bold (brighter foreground), inverse (swap
/// fg/bg), underline (1px bottom-edge line), and dim (darker foreground).
fn render_cell(
    image: &mut RgbaImage,
    pixel_x: u32,
    pixel_y: u32,
    character: char,
    foreground: CellColor,
    background: CellColor,
    style: CellStyle,
) {
    let (text_color, fill_color) = if style.inverse() {
        (background, foreground)
    } else {
        (foreground, background)
    };

    let final_fg = apply_brightness(text_color, style);
    let bg_rgba = color_to_rgba(fill_color);
    let fg_rgba = color_to_rgba(final_fg);

    // Fill background.
    fill_rect(image, pixel_x, pixel_y, CELL_WIDTH, CELL_HEIGHT, bg_rgba);

    // Draw character glyph.
    let glyph = lookup_glyph(character);
    draw_glyph(image, pixel_x, pixel_y, glyph, fg_rgba);

    // Underline: 1px line at the bottom edge.
    if style.underline() {
        let underline_y = pixel_y + CELL_HEIGHT - 1;
        for offset_x in 0..CELL_WIDTH {
            let target_x = pixel_x + offset_x;
            if target_x < image.width() && underline_y < image.height() {
                image.put_pixel(target_x, underline_y, fg_rgba);
            }
        }
    }
}

/// Apply brightness adjustments based on style flags.
///
/// Bold increases brightness by ~40%, dim decreases by ~40%.
fn apply_brightness(color: CellColor, style: CellStyle) -> CellColor {
    if style.bold() {
        CellColor::new(
            brighten_component(color.red),
            brighten_component(color.green),
            brighten_component(color.blue),
        )
    } else if style.dim() {
        CellColor::new(
            dim_component(color.red),
            dim_component(color.green),
            dim_component(color.blue),
        )
    } else {
        color
    }
}

/// Increase a color component by ~40%, clamped to 255.
fn brighten_component(value: u8) -> u8 {
    let brightened = u16::from(value) * 140 / 100;

    u8::try_from(brightened.min(255)).unwrap_or(255)
}

/// Decrease a color component by ~40%.
fn dim_component(value: u8) -> u8 {
    let dimmed = u16::from(value) * 60 / 100;

    u8::try_from(dimmed).unwrap_or(0)
}

/// Fill a rectangular region with a solid color.
fn fill_rect(
    image: &mut RgbaImage,
    start_x: u32,
    start_y: u32,
    width: u32,
    height: u32,
    color: Rgba<u8>,
) {
    for offset_y in 0..height {
        for offset_x in 0..width {
            let target_x = start_x + offset_x;
            let target_y = start_y + offset_y;
            if target_x < image.width() && target_y < image.height() {
                image.put_pixel(target_x, target_y, color);
            }
        }
    }
}

/// Draw a bitmap glyph at the given pixel position.
fn draw_glyph(
    image: &mut RgbaImage,
    pixel_x: u32,
    pixel_y: u32,
    glyph: &[u8; 16],
    color: Rgba<u8>,
) {
    for (glyph_row, &byte) in glyph.iter().enumerate() {
        let row_offset = u32::try_from(glyph_row).unwrap_or(0);
        for bit in 0..8u32 {
            if byte & (0x80 >> bit) != 0 {
                let target_x = pixel_x + bit;
                let target_y = pixel_y + row_offset;
                if target_x < image.width() && target_y < image.height() {
                    image.put_pixel(target_x, target_y, color);
                }
            }
        }
    }
}

/// Convert a [`CellColor`] to an [`Rgba`] pixel.
fn color_to_rgba(color: CellColor) -> Rgba<u8> {
    Rgba([color.red, color.green, color.blue, 255])
}

